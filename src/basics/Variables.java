package src.basics;

public class Variables {
  public static void execute() {
    System.out.println("=== Variables ===");

    int number = 120;
    System.out.println(number);

    double pi = 3.14;
    System.out.println(pi);

    char letter = 'R';
    System.out.println(letter);

    boolean maybe = true;
    System.out.println(maybe);

    String word = "amazing";
    System.out.println(word);
  }
}
